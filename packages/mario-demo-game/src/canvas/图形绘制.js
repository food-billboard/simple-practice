		_drawCircle: function(context, x, y, a, b) {	//椭圆的绘制方法;	
			var r = (a > b) ? (1 / a) : (1 / b);
			context.moveTo(x + r, y);
			for(var i = 0, len = Math.PI * 2; i < len; i += r) {
				context.lineTo(x + a * Math.cos(i), y + b * Math.sin(i));
			}
		},
